def turysta(G, D, L):
    q = PriorityQueue()
    print(len(G))

    max_ = max(x[1] for x in G) + 1
    graph_list = [[] for _ in range(max_)]

    for x in G:
        graph_list[x[0]].append((x[1], x[2]))
        graph_list[x[1]].append((x[0], x[2]))

    for x in range(len(graph_list)):
        graph_list[x].sort(key=lambda y: (y[0], y[1]))
        if len(graph_list[x]) > 1:
            i = 0
            curr = []
            while i < len(graph_list[x]):
                j = i
                curr.append(graph_list[x][j])
                while i < len(graph_list[x]) and graph_list[x][i][0] == graph_list[x][j][0]:
                    i += 1
            graph_list[x] = curr[:]

    for edges in graph_list[D]:
        #    waga,  wierzchołek,co było,stopień
        q.put((edges[1], -1, edges[0], [D]))

    # for k in graph_list:
