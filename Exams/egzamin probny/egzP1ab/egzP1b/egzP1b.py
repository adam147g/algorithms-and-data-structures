    while not q.empty():
        weight, level, vertex, was = q.get()
        if vertex == L:
            continue
        for edges in graph_list[vertex]:
            if edges[0] == L and level == -3:
                return weight + edges[1]
            if not edges[0] in was and level > -3:
                q.put((weight + edges[1], level - 1, edges[0], was + [vertex]))

    return None


